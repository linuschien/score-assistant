package com.scoreassistant.adapter.out.persistence;

import com.scoreassistant.domain.model.*;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.transaction.annotation.Transactional;
import reactor.test.StepVerifier;

import java.math.BigDecimal;
import java.time.LocalDate;
import java.time.LocalDateTime;
import java.util.UUID;

import static org.junit.jupiter.api.Assertions.*;

@SpringBootTest(webEnvironment = SpringBootTest.WebEnvironment.MOCK)
@DisplayName("R2DBC Repository Integration Tests with H2")
class RepositoryIntegrationTest {

    @Autowired SemesterRepository semesterRepository;
    @Autowired ClassRepository classRepository;
    @Autowired StudentRepository studentRepository;
    @Autowired GradeItemRepository gradeItemRepository;
    @Autowired GradeRecordRepository gradeRecordRepository;
    @Autowired AttachmentRepository attachmentRepository;

    @Test
    @DisplayName("Should successfully insert and retrieve a Semester using BeforeConvertCallback")
    void shouldInsertAndRetrieveSemester() {
        var now = LocalDateTime.now();
        var semester = new SemesterEntity(
                null, // Passing null ID to trigger BeforeConvertCallback UUID generation
                "2026-Fall-Integration",
                LocalDate.of(2026, 9, 1),
                LocalDate.of(2027, 1, 31),
                now, now, null
        );

        // 1. Save (INSERT)
        var savedSemesterMono = semesterRepository.save(semester);

        StepVerifier.create(savedSemesterMono)
                .assertNext(saved -> {
                    assertNotNull(saved.id(), "UUID should be generated by SemesterBeforeConvertCallback");
                    assertEquals("2026-Fall-Integration", saved.semesterName());
                    assertNotNull(saved.createdAt());
                })
                .verifyComplete();
    }

    @Test
    @DisplayName("Should successfully insert a Class linked to a Semester")
    void shouldInsertClass() {
        var now = LocalDateTime.now();
        var semester = new SemesterEntity(
                null,
                "2026-Fall-Class-Test",
                LocalDate.of(2026, 9, 1),
                LocalDate.of(2027, 1, 31),
                now, now, null
        );

        var savedSemester = semesterRepository.save(semester).block();
        assertNotNull(savedSemester);

        var clazz = new ClassEntity(
                null, // Passing null ID to trigger BeforeConvertCallback
                savedSemester.id(),
                "CS-202",
                BigDecimal.valueOf(60.0),
                now, now, null
        );

        StepVerifier.create(classRepository.save(clazz))
                .assertNext(saved -> {
                    assertNotNull(saved.id(), "UUID should be generated by ClassBeforeConvertCallback");
                    assertEquals(savedSemester.id(), saved.semesterId());
                    assertEquals("CS-202", saved.className());
                })
                .verifyComplete();
    }

    @Test
    @DisplayName("Should successfully insert Student, GradeItem, GradeRecord and Attachment sequentially")
    void shouldInsertFullEntityHierarchy() {
        var now = LocalDateTime.now();

        // 1. Create Semester
        var semester = new SemesterEntity(
                null, "2026-Fall-Hierarchy-Test",
                LocalDate.of(2026, 9, 1), LocalDate.of(2027, 1, 31),
                now, now, null
        );
        var savedSemester = semesterRepository.save(semester).block();
        assertNotNull(savedSemester);

        // 2. Create Class
        var clazz = new ClassEntity(
                null, savedSemester.id(), "CS-303", BigDecimal.valueOf(60.0),
                now, now, null
        );
        var savedClass = classRepository.save(clazz).block();
        assertNotNull(savedClass);

        // 3. Create Student
        var student = new StudentEntity(
                null, savedClass.id(), 20260909, "Integration Bob",
                now, now, null
        );
        var savedStudent = studentRepository.save(student).block();
        assertNotNull(savedStudent);

        // 4. Create GradeItem
        var gradeItem = new GradeItemEntity(
                null, savedClass.id(), "Final Project", "ASSIGNMENT",
                LocalDate.now(), "Final project integration test",
                BigDecimal.valueOf(100.0), BigDecimal.valueOf(0.4),
                now, now, null
        );
        var savedGradeItem = gradeItemRepository.save(gradeItem).block();
        assertNotNull(savedGradeItem);

        // 5. Create GradeRecord
        var gradeRecord = new GradeRecordEntity(
                null, savedGradeItem.id(), savedStudent.id(),
                BigDecimal.valueOf(95.5), now, 0,
                now, now, null
        );
        var savedGradeRecord = gradeRecordRepository.save(gradeRecord).block();
        assertNotNull(savedGradeRecord);

        // 6. Create Attachment
        var attachment = new AttachmentEntity(
                null, savedGradeRecord.id(), "project_spec.pdf", "application/pdf",
                128000, new byte[]{4, 5, 6}, now, now, now, null
        );
        var savedAttachment = attachmentRepository.save(attachment).block();
        assertNotNull(savedAttachment);

        // Assert all generated IDs are not null
        assertNotNull(savedStudent.id(), "Student ID must be generated");
        assertNotNull(savedGradeItem.id(), "GradeItem ID must be generated");
        assertNotNull(savedGradeRecord.id(), "GradeRecord ID must be generated");
        assertNotNull(savedAttachment.id(), "Attachment ID must be generated");

        assertEquals(20260909, savedStudent.studentNumber());
        assertEquals("project_spec.pdf", savedAttachment.fileName());
    }
}
